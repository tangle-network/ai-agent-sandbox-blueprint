#!/usr/bin/env node
/**
 * Generates `ui/src/lib/contracts/abi.ts` from Foundry build artifacts.
 *
 * The hand-maintained ABI drifted from the deployed contract (wrong constructor
 * arity, missing lifecycle functions). This script makes the forge artifact the
 * single source of truth so the UI's ABIs can never silently diverge again.
 *
 * Usage:
 *   node scripts/gen-contract-abi.mjs            # write ui/src/lib/contracts/abi.ts
 *   node scripts/gen-contract-abi.mjs --out FILE # write to FILE (used by gen:abi:check)
 *
 * No external dependencies: plain Node ESM reading the JSON artifact directly.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');

const DEFAULT_OUT = resolve(repoRoot, 'ui/src/lib/contracts/abi.ts');

/**
 * Each UI export is bound to one forge artifact. There is a single on-chain
 * contract (`AgentSandboxBlueprint`) that serves the sandbox, instance, and
 * tee-instance modes via constructor flags, so both UI exports resolve to the
 * same artifact. The instance ABI is a strict subset of the full ABI; pointing
 * both exports at the full artifact keeps a single source of truth and removes
 * the drift that existed when the subset was maintained by hand.
 */
const EXPORTS = [
  {
    name: 'agentSandboxBlueprintAbi',
    artifact: 'contracts/out/AgentSandboxBlueprint.sol/AgentSandboxBlueprint.json',
    doc: 'Full AgentSandboxBlueprint ABI (sandbox/cloud mode + shared surface).',
  },
  {
    name: 'agentInstanceBlueprintAbi',
    artifact: 'contracts/out/AgentSandboxBlueprint.sol/AgentSandboxBlueprint.json',
    doc:
      'AgentSandboxBlueprint ABI for instance / TEE-instance deployments. Same\n' +
      ' * contract as the sandbox blueprint, deployed with instanceMode=true.',
  },
];

function loadAbi(artifactRelPath) {
  const artifactPath = resolve(repoRoot, artifactRelPath);
  let raw;
  try {
    raw = readFileSync(artifactPath, 'utf8');
  } catch (err) {
    throw new Error(
      `Cannot read forge artifact at ${artifactRelPath}. ` +
        `Run \`forge build\` from the repo root first. (${err.message})`,
    );
  }
  const json = JSON.parse(raw);
  if (!Array.isArray(json.abi)) {
    throw new Error(`Artifact ${artifactRelPath} has no \`abi\` array.`);
  }
  return json.abi;
}

function renderExport({ name, artifact, doc }) {
  const abi = loadAbi(artifact);
  // 2-space indentation to match the repo style; deterministic ordering is
  // whatever the compiler emitted, which is stable across rebuilds.
  const body = JSON.stringify(abi, null, 2);
  return `/**\n * ${doc}\n * Source: ${artifact}\n */\nexport const ${name} = ${body} as const;\n`;
}

function render() {
  const header =
    '// AUTO-GENERATED — DO NOT EDIT.\n' +
    '// Generated by scripts/gen-contract-abi.mjs from Foundry artifacts.\n' +
    '// Regenerate: `npm run gen:abi` (in ui/) after changing contracts.\n' +
    '// CI guard: `npm run gen:abi:check` fails if this file is stale.\n\n';
  return header + EXPORTS.map(renderExport).join('\n');
}

function main() {
  const args = process.argv.slice(2);
  const outIdx = args.indexOf('--out');
  const outPath = outIdx !== -1 ? resolve(process.cwd(), args[outIdx + 1]) : DEFAULT_OUT;

  const content = render();
  writeFileSync(outPath, content);
  process.stderr.write(`Wrote ${outPath} (${EXPORTS.length} export(s)).\n`);
}

main();
